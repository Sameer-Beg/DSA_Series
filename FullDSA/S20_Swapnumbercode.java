package DSA_Series.FullDSA;

public class S20_Swapnumbercode {
    public static void main(String[] args) {
        int a = 90;
        int b  = 100;

        int temp = a ;
        a = b;
        b = temp  ;
        System.out.println("Know a is ::" + a);
        System.out.println("Know b is ::" + b);
    }
}
