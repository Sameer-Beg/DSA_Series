package DSA_Series.FullDSA;

public class S8_Loops {
    public static void main(String[] args) {
        /*
        The syntax of For loop
        for(initialization ; condition ; increment/decrement){
                //body
            }
         */
//        program print 1 to 100
        for (int i =  0 ; i<=100 ; i++){
            System.out.println(i);
        }

    }
}
